package org.example.clase2;

public class Ej2 {
    public static void main(String[] args) {
        System.out.println("Esto es un numero entero:");
        System.out.println(10);

        System.out.println("Esto es un numero decimal:");
        System.out.println(10.23);

        System.out.println("Esto es un caracter:");
        System.out.println('h');

        System.out.println("Estos son booleanos:");
        System.out.println(true);
        System.out.println(false);
    }
}
